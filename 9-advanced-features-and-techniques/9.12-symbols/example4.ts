// TS uses symbols for internal language behaviors:

Symbol.asyncIterator; // Method the returns an AsyncIterator object
Symbol.hasInstance; // Method that determines if a constructor object recognizes an object as its instance
Symbol.isConcatSpreadable; // Boolean value that indicates if an object should be flattened to its array elements by Array.prototype.concat
Symbol.iterator; // Method that returns an iterator for an object
Symbol.match; // Method that matches a string against a regular expression
Symbol.replace; // Method that replaces matched substrings of a string
Symbol.search; // Method that returns the index of the first match of a string against a regular expression
Symbol.species; // Constructor function used to create derived objects
Symbol.split; // Method that splits a string into an array of substrings
Symbol.toPrimitive; // Method that converts an object to a primitive value
Symbol.toStringTag; // Method that returns the default description of an object
Symbol.unscopables; // Object that defines properties that are excluded from with bindings
